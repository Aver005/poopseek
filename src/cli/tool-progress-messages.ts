const FALLBACK_MESSAGES = [
    "Кручу внутренние шестерёнки агента ⚙️",
    "Выполняю задачу и стараюсь не разрушить вселенную 🌌",
];

function getRandomItem(items: string[]): string
{
    const randomIndex = Math.floor(Math.random() * items.length);
    return items[randomIndex] ?? FALLBACK_MESSAGES[0] ?? "Выполняю задачу...";
}

export function getToolProgressMessage(toolName: string): string
{
    const scopedMessages = TOOL_MESSAGES[toolName];
    if (scopedMessages && scopedMessages.length > 0)
        return adaptTextToTerminal(getRandomItem(scopedMessages));

    return adaptTextToTerminal(getRandomItem(FALLBACK_MESSAGES));
}

export function getToolDetail(toolName: string, args: Record<string, unknown>): string | null
{
    const path = typeof args.path === "string" ? args.path : null;
    const command = typeof args.command === "string" ? args.command : null;

    switch (toolName)
    {
        case "file.read":
        case "file.write":
        case "file.edit":
        case "file.remove":
        case "git.edit":
            return path ? basename(path) : null;
        case "file.list":
            return path ?? null;
        case "bash":
        case "powershell":
        case "git":
            if (!command) return null;
            return command.length > 100 ? `${command.slice(0, 100)}…` : command;
        default:
            return null;
    }
}
